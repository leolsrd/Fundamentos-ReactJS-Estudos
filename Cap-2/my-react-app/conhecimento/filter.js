// ? Filter
// ? Filter ele serve para filtrar elementos de um array/ lista com base em
// ? uma condição e ele retorna um nova array/ lista contendo os elementos que
// ? satisfizeram a condição.

const number = [1, 2, 3, 4, 5];
const evens = number.filter((num) => num % 2 === 0);
console.log(evens);
